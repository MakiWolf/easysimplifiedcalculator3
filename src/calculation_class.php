<?php
function add($number1, $number2) {
    $result = $number1 + $number2;
    return $result;
}
function sub($number1, $number2) {
    $result = $number1 - $number2;
    return $result;
}
function mult($number1, $number2) {
    $result = $number1 * $number2;
    return $result;
}
function div($number1, $number2) {
    $result = $number1 / $number2;
    return $result;
}
?>